#include <iostream>
using namespace std;

//Programa que muestra del 1 al 100 de 5 en 5
int main() {

    cout << "\n==================================" << endl;
    //Variables
    int n = 0;

    while (n<=100){
        cout << n << "   ";
        n+=5;
    }
    cout << "\n==================================" << endl;

}